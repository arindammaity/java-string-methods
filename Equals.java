package ilp;
import java.util.*;
public class Equals
{
	public static void main(String[] args)
	{
		Scanner sc = new Scanner(System.in);
		String x = sc.nextLine();
		String y = sc.nextLine();
		if(x.equals(y))
		{
			System.out.println("true");
		}
		else
		{
			System.out.println("false");
		}
	}

}
